  <div class="sousTitre">
    <h2>Le Bois de Vincennes, une forêt royale ayant subi de nombreux changements</h2>
    </div>
    <div class="center-text2">
    <p>Historiquement, le bois de Vincennes était avant tout connu comme un terrain de chasse pour les rois de France.</p>
    </div>
    <div class="center-text2">
    <p>Cette déclaration d'usage royale exclusive remonterait à Hugues Capet (Xè siècle). En effet, c'est lorsque ce dernier décide de fixer sa résidence sur l'île de la Cité (abritant notamment Notre-Dame) que le bois devient un terrain de chasse. Par la suite, cette terre demeurera à usage royale.</p>
    </div>
    <div class="center-text2">
    <p>En 1183, sous Philippe Auguste , le bois fut enclosé et approvisionner en gibier par une enceinte de 12km de long, qui restera alors en place pendant dix siècles, avant d'être détruit sous le Second Empire. Philippe Auguste reconstitua également le bois : les abbayes environnantes du bois avaient empiété sur le bois jusqu'à le réduire à 17 hectares. Au fil du temps et des souverains, le château et ses différents éléments furent bâtis.</p>
  </div>
  <div class="center-text2">
    <p>C'est sous Louis XV que le bois devient accessible au public. La forêt est toujours une réserve de chasse, et la coupe de ses arbres est réglementé pour assurer la pérennité de celle-ci. S'en suivra alors, au fil des années et des siècles, des mouvements de militarisation, de pillage et de construction, pendant notamment la révolution française, les Empires (Napoléon), et la Seconde Guerre Mondiale.</p>
  </div>
